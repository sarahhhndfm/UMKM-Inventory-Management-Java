    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    PrinterJob job = PrinterJob.getPrinterJob();
    job.setPrintable(new Printable() {
        @Override
        public int print(Graphics g, PageFormat pf, int pageIndex) throws PrinterException {
            if (pageIndex > 0) {
                return NO_SUCH_PAGE;
            }

            // Mengatur posisi dan ukuran untuk mencetak
            g.drawString("================================", 100, 100);
            g.drawString("Struk Transaksi", 100, 120);
            g.drawString("Tanggal: " + new SimpleDateFormat("dd-MM-yyyy").format(dateTanggal.getDate()), 100, 140);
            g.drawString("Variasi        Harga     Jumlah     Total", 100, 160);
            g.drawString("================================", 100, 180);

            // Menampilkan data dari tabel
            for (int i = 0; i < tblTampilan.getRowCount(); i++) {
                String variasi = tblTampilan.getValueAt(i, 0).toString();
                String harga = tblTampilan.getValueAt(i, 1).toString();
                String jumlah = tblTampilan.getValueAt(i, 2).toString();
                String total = tblTampilan.getValueAt(i, 3).toString();
                g.drawString(variasi + "        " + harga + "     " + jumlah + "     " + total, 100, 200 + (i * 20));
            }

            g.drawString("================================", 100, 200 + (tblTampilan.getRowCount() * 20));
            g.drawString("Total Bayar: " + txtTotalBayar.getText(), 100, 220 + (tblTampilan.getRowCount() * 20));
            g.drawString("Bayar: " + txtbayar.getText(), 100, 240 + (tblTampilan.getRowCount() * 20));
            g.drawString("Kembalian: " + txtkembalian.getText(), 100, 260 + (tblTampilan.getRowCount() * 20));
            g.drawString("================================", 100, 280 + (tblTampilan.getRowCount() * 20));
            g.drawString("Terima kasih!", 100, 300 + (tblTampilan.getRowCount() * 20));

            return PAGE_EXISTS;
        }
    });

    // Menampilkan dialog untuk memilih printer
    if (job.printDialog()) {
        try {
            job.print();
        } catch (PrinterException e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

