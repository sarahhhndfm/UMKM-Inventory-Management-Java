    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    DefaultTableModel model = (DefaultTableModel) tblTampilan.getModel();

    String tanggal;
    if (dateTanggal.getDate() != null) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        tanggal = sdf.format(dateTanggal.getDate());
    } else {
        tanggal = null;
    }

    String total_harga = txtTotalBayar.getText();
    String uang_bayar = txtbayar.getText();
    String uang_kembalian;

        // Validasi input
        if (tanggal == null || total_harga.isEmpty() || uang_bayar.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Konversi string ke integer untuk menghitung uang kembalian
            int totalHarga = Integer.parseInt(total_harga);
            int uangBayar = Integer.parseInt(uang_bayar);

            // Hitung uang kembalian
            int kembalian = uangBayar - totalHarga;
            uang_kembalian = String.valueOf(kembalian);

            // Cek apakah uang bayar cukup
            if (kembalian < 0) {
                JOptionPane.showMessageDialog(this, "Uang bayar tidak cukup!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Simpan ke database
            Connection c = koneksi.getConnection();
            String sql = "INSERT INTO penjualan (tanggal, total_harga, uang_bayar, uang_kembalian) VALUES (?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, tanggal);
            p.setInt(2, totalHarga);
            p.setInt(3, uangBayar);
            p.setInt(4, kembalian);

            p.executeUpdate();
            p.close();

            JOptionPane.showMessageDialog(this, "Data penjualan berhasil disimpan!");

            // Bersihkan input setelah menyimpan
            txtTotalBayar.setText("");
            txtbayar.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Pastikan Total Harga dan Uang Bayar adalah angka valid!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            System.err.println("Simpan penjualan error: " + e.getMessage());
        }

        
        try {
            Connection c = koneksi.getConnection();
            int baris = tblTampilan.getRowCount();
            
            for (int i = 0; i < baris; i++) {
                String sql = "INSERT INTO rincianpenjualan (nama_variasi, harga, jumlah_beli, total_harga) VALUES ('"
                        + tblTampilan.getValueAt(i, 0) +"','"+ tblTampilan.getValueAt(i, 1) +"','"+ tblTampilan.getValueAt(i, 2)
                        +"','"+ tblTampilan.getValueAt(i, 3) +"')";
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
            }
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Simpan rincianpenjualan error");
        }
        utama();
        clear();
        kosong();
        txtTampil.setText("Rp. 0");
    }//GEN-LAST:event_jButton2ActionPerformed

